    {
        //
        $data=$request->all();
        $todos=FollowModel::where('user_id','=',$data['user_id'])->get();
        
        $updates=array();
        
        foreach($todos as $todo){
            $todo2=UpdatesModel::where('farmer_username','=',$todo['farmer_username'])->get();
            array_push($updates,$todo2);
            
        }
        return response()->json(["message"=>"OK",
                                "data"=>$updates],200);
                        
    }

